life_stage = 16
if life_stage < 0:
    print("The person is a fetus")
elif life_stage < 4:
    print("The person is a toddler")
elif life_stage < 11:
    print("The person is a child")
elif life_stage < 18:
    print("The person is a young adult")
elif life_stage < 57:
    print("The person is middleaged")
else:
    print("The person is an elderly")